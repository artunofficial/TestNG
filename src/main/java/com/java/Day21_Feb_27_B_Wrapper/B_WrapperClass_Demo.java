package com.java.Day21_Feb_27_B_Wrapper;

public class B_WrapperClass_Demo {

	//int - Integer
	//long - Long
	//short - Short
	//boolean - Boolean
	//double - Double
	//char - Character
	//float - Float
	//byte - Byte
	
	//Wrapper Classes are pre-defined classes which can contain primitve data type.
	//They wrap primitve data type to Wrapper Class Object
	
	public static void main(String[] args) {
		int a = 10;
		System.out.println("The primitive data type value is : " + a);
		
		Integer b = 100;
		System.out.println("The Wrapper class object value is : + b");
		//b. will show off Wrapper Classes
	}
}
