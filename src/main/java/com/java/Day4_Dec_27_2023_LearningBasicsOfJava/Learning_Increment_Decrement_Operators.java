public class Learning_Increment_Decrement_Operators {

	public static void main(String[] args) {
		
		//Arithmetic Operators ++ (increment) -- (decrement)
		// ++ represents +1, -- represents -1
		
		
		int 			i 			= 			1; //this was the oldest value of i
		//datatype		variable				
		// in the memory location of java the variable i has a value stored as 1
		
		i = 2; // we have updated the value of variable 1. earlier it was 1 and now it has been updated to 2
		i = 3; 
		System.out.println("The lates value of i is : " + i);
		i = 4; 
		i = 5; // this is the lates value of i
		
		System.out.println("The lates value of i is : " + i);
		
		
		
		

	}

}
